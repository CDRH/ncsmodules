<p>This site is designed to provide students of the nineteenth century in Europe and the United States resources in literature, history, art, and material culture to carry out interdisciplinary study of major themes and topics that help define the nature of the era.</p>         

<p>The "long nineteenth century" stretches from the rise of Romanticism in the eighteenth century to the rise of Modernism in the early twentieth century.  Within such a long period as a century, or even the "Victorian" period (1837-1901), there are many different currents of cultural history.  This site will ultimately feature eight modules, each of which attempts to capture these currents of history through a focus on a given theme or topic.  Some modules, such as "<a href="loveandseduction/">Scenes of Love and Seduction</a>," also focus on a shorter time frame within the century (for instance, 1840-1870).</p>

<p>What does "interdisciplinary" study mean, and what should be its goals?  Interdisciplinary study may mean simply taking the insights from one discipline to help us see more deeply into another, as a history of the Battle at Waterloo might enable us to understand better the episode in Thackeray's Vanity Fair when the people at the dance in Brussels are thrown into panic by the sounds of cannons at the edge of the city.  Ideally, interdisciplinary study means more than that.  Our goal is to understand culture in its broadest sense, to place historical events in the context of custom and culture, to examine how the artistic representations and the popular culture reveal how people felt and thought about their own world and time. To see how such things as class, education, nationality, geographic setting, religion, gender, and work might affect experience, we need to look across a wide range of representations from different perspectives.  We need only consider how differently slavery, revolution, civil war, or industrial development looked depending on who was experiencing them and from what position to remember that there are many perspectives and influences that color what we understand about history and cultural experience.</p> 

<p>The nineteenth century was the first historical period in which the speed of steam-powered travel and innovations in print technology allowed the countries of Europe to be in close contact with each other. The influence of Europe, especially Great Britain, in American culture extended the community of common concerns. The electronic technology that allows us to assemble interdisciplinary resources also means that a growing global communication brings fresh importance to looking at issues and themes across national boundaries.  No site can be comprehensive; this one looks with particular attention to the commonalities in American, British, and European societies.</p>

 
<p>Nineteenth Century Studies: Resources for an Interdisciplinary Curriculum is designed to provide a structure for looking at eight major topics of common interest across several cultures.  The resources include historical documents, literary works, popular fiction, journalism, public art, paintings, illustrations, and scholarly commentary.  Each section will provide a brief overview to get viewers started thinking about the topic and which will raise some of the questions the resources might inspire for further discussion.  Viewers may also search the full site to discover further intersections among topics.</p>

<p>This site is maintained by the <a href="http://www.unl.edu/19thcentury/">Nineteenth-Century Studies program</a> at the <a href="http://www.unl.edu/">University of Nebraska&ndash;Lincoln</a>.  We are very interested in making the resources here as easy to use as possible. The editors invite your comments and suggestions.</p>

